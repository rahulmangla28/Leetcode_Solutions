class Solution {
public:
    vector<long long> findPrefixScore(vector<int>& nums) {
        
        int n =nums.size();
    
        
        
        
        
        
        
        
        
        
        vector<long long> till_maxi(n);
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        long long maxi = 0;
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        for(int i=0;i<n;i++) {
        
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            if(nums[i]>=maxi) maxi = nums[i];
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            till_maxi[i] = maxi;
        }
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        vector<long long> alpha(n);
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        for(int i=0;i<n;i++) {
        
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            alpha[i] = nums[i] + till_maxi[i];
        }
        
       
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        vector<long long> preffi(n);
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        preffi[0] = alpha[0];
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        
        for(int i=1;i<n;i++) {
        
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            
            preffi[i] = alpha[i] + preffi[i-1] ;
        }
        
        return preffi;
        
    }
};